                        <h3>4. Section Drawings </h3>
                        <p>	Section drawings are a specific type of drawing architects use to illustrate a building or portion of a building. A section is drawn from a vertical plane slicing through a building. This type of construction drawing helps to identify the primary structures in relation to other surrounding structures of the building. In addition, the section reveals simultaneously its interior and exterior profiles and providing a view of the object that is not usually seen.</p>
                        
